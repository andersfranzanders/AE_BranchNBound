package wrappers;

public class ResultOfMeasurement {
	
	public double time;
	public int numberOfSlots;
	public double violationsPerSlots;
	
	
	@Override
	public String toString() {
		return "ResultOfMeasurement [time=" + time + ", numberOfSlots=" + numberOfSlots + ", violationsPerSlots="
				+ violationsPerSlots + "]";
	}
	
	

}
